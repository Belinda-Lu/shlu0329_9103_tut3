function musicButton(x, y, ang) {
  push();
  translate(x, y);
  rotate(radians(ang));
  scale(1, offsetScale);
  stroke(0);
  strokeWeight(4);
  line(0, 0, 0, -25);
  line(-10, -25, 10, -25);
  pop();
}

function note(x, y, c, ang) {
  push();
  translate(x, y);
  rotate(radians(ang));
  fill(c);
  ellipse(0, 0, 20, 15);
  stroke(c);
  strokeWeight(4);
  line(8, 0, 8, -40);
  pop();
}
